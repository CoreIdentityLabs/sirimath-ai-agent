export function parseQuery(text: string): string {
	const tokens = text
		.toLowerCase()
		.split(/\W+/)
		.filter((t) => t.length > 0 && !STOP_WORDS.has(t));

	const unique = [...new Set(tokens)].slice(0, 10);

	if (unique.length < 2) {
		return text.trim();
	}

	return unique.join(" ");
}
